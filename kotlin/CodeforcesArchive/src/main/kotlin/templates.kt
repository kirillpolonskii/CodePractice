fun readInt() = readln().toInt()
fun readLong() = readln().toLong()
fun readDouble() = readln().toDouble()
fun readFloat() = readln().toFloat()

fun readIntList(delim: String) = readln().split(delim).map{it.toInt()}
fun readLongList(delim: String) = readln().split(delim).map{it.toLong()}
fun readDoubleList(delim: String) = readln().split(delim).map{it.toDouble()}